#include <iostream>
#include <cmath>

using namespace std;
int main()
{
    double a = 30;
    cout << cos(a) << endl;
    cout << sin(a) << endl;
    cout << tan(a) << endl;
    cout << log(a) << endl;
    cout << pow(a, 2) << endl;
    cout << hypot(a, 40) << endl;
    cout << sqrt(a) << endl;
    cout << abs(-30) << endl;
    
    return 0;    
}